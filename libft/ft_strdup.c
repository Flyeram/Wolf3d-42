#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s2;
	int		len;
	int		i;

	i = 0;
	len = ft_strlen(s1);
	s2 = ft_strnew(len + 1);
	if (s2 != NULL)
	{
		while (i < len)
		{
			s2[i] = s1[i];
			i++;
		}
	}
	return (s2);
}
